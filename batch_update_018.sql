Cumapol Emmen BV
The mission of Cumapol is to produce sustainable thermoplastic Polyester specialties by combining the different recycling technologies, close the Polyester loop and create a high performance product without using fossil based chemicals.

Cumapol Emmen is a toll converter serving the PET industry by converting amorphous, low IV modified Polyesters into valuable PET grades suitable for the intended application.

Cumapol offers different services in Polyester upgrading: Regranulation, Compounding, Crystallization and Solid State Polymerization for a wide range of virgin and recycled Polyesters. Cumapol has the positive expert opinion from EFSA, which means that recycled material processed by Cumapol can be reused in most direct food contact applications.

In 2013, we started with the recycling of post-consumer bottles, back into a bottle grade suitable for direct food contact and the development of special grades based on recycled materials. Nowadays, 50% of our capacity is occupied with the upgrading of recycled PET and 50% with virgin specialties with experience in PET, Copolyesters, PBT, PEF, etc.

We are continuously improving our custom made Polyesters based on mechanical recycling. With the start of exploring chemical recycling and the use of bio-based monomers we made the next steps in circularity but we have many plans to improve, please check our website www.cumapol.com or contact us for any questions.
