Sirmax S.p.A.
Sirmax Group, with headquarters in Cittadella (Padua), is Europe’s leading independent manufacturer, and among the top global manufacturers, of polypropylene compounds, engineering polymers, post-consumer compounds and bio-compounds used across all sectors: automotive, household appliances, power tools, electrical, electronics, construction, furniture, agriculture and packaging (the latter two through innovative compostable biopolymers).

Active since the 1960s, Sirmax now boasts 13 production plants: Six in Italy (Cittadella, Tombolo, Isola Vicentina, San Vito Al Tagliamento, Salsomaggiore Terme, and Mellaredo di Pianiga), two in Poland (Kutno), one in Brazil (Jundiai), two in the US (Anderson) and two in India (Palwal, Valsad), employing 850 people. Sirmax also has a sales office in Milan, Italy, and other branches in France, Spain, and Germany. Sirmax has acquired significant market shares in Europe, North & South America, and Asia, and has become a global player on the international market.

In recent years, Sirmax has integrated recycling into its production , acquiring dedicated plants for recycling post-consumer and post-industrial plastics in both Europe and America. This vertical integration enables the company to transform plastic waste into high-quality products, promoting a circular economy model and meeting the growing demand for sustainable materials.

