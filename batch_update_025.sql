traceless materials GmbH
traceless materials GmbH is a female-founded bioeconomy startup based in Hamburg, Germany.  With traceless®, we are offering a novel material alternative to conventional plastics and bioplastics.  Based on our innovative, patent-pending technology, we use agricultural industry by-products to produce natural biopolymer materials that provide many beneficial properties of plastics, while being fully bio-circular and sustainable considering all impact indicators.

Even though traceless® is plantbased, naturally compostable and plastic free, it is suitable for many plastic applications and converting technologies. We offer our material as a base material in granulate form, which can be further processed to flexible films, rigid applications, coatings or adhesive solutions with many standard thermoplastic technologies. traceless® can replace plastics and bioplastics in a wide range of use cases – from rigid and flexible packaging to disposables to products with high abrasion and paper coating & adhesive solutions.

Our novel solution is part of a new generation of natural polymer biomaterials, going one decisive step beyond the sustainability of currently available biobased and biodegradable plastics.

It is our mission to jointly realize the biggest possible contribution to tackling global pollution and waste challenges. With traceless®, we enable manufacturers and brand owners to become a part of this mission: By developing innovative plastic-free products from traceless®, they can fulfil sustainability goals, meet legal demands and be at the forefront of innovative green product development.
