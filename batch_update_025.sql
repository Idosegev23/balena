Advanced Petrochemical Company JOINT STOCK
PREMIUM
Advanced was incorporated as a closed joint stock company on October 01, 2005, with current paid-up capital of SR 2,600,000,000. The company was listed on the Saudi Exchange in 2006.

Advanced Petrochemical Company “Advanced” is engaged in the manufacture of plastics in primary forms, investment in industrial projects including, but not limited to, projects relating to petrochemical, chemical, basic and conversion industries and industries relating to renewable energy and to establish, operate and own industrial projects both within and outside of the Kingdom of Saudi Arabia.

In May 2006, Advanced started the construction of its integrated petrochemical plants, located in Jubail Industrial City in the Eastern Province of the Kingdom of Saudi Arabia, which started its commercial production from its polypropylene facility on March 03, 2008. The integrated plant is designed to produce 455,000 tons per year of propylene and 450,000 tons per year of polypropylene (nameplate capacities) from its production facility.

Advanced is currently executing its Propane Dehydrogenation, with a total nameplate capacity of 843,000 tons per year of Propylene using CATOFIN Technology and Polypropylene (PP) plants, with a total nameplate capacity of 800,000 tons per year using Spheripol and Spherizone technologies from LyondellBasell, with approximately twice its current size, which is expected to come on stream by Q3 2024. This project aims to produce various specialized PP product grades which will provide an edge for Advanced to penetrate value-added PP segments.
