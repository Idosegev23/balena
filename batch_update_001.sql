Delta Kunststoffe AG
Delta Kunststoffe AG is an experienced compounder and has been part of the international Kafrit Group since 2022. Specialties are color formulations, electrically conductive plastics and special compounds as well as additives and contract compounding.

As a company founded in 1994, we develop new customized materials for logistics, construction, artificial turf, sporting goods, household and toys, automotive and electrical engineering with almost 80 employees in Weeze on the Lower Rhine. We work closely with raw material producers, plastics processors, plastics users, universities and research institutes.

Our product range of colour formulations includes colour preparations, colour masterbatches, pigment combinations, UV stabilizers and additives for process optimization, additive masterbatches and dry pigments. The portfolio of electrically conductive compounds ranges from conductive plastics, permanently antistatic compounds and intrinsically conductive compounds to compounds with nanoscale fibers and fillers. Individual compounds include materials with their own character, construction and special materials as well as special compounds.

In the field of contract compounding, our modern facilities guarantee high-quality products and enable us to compound the complete range of specialty plastics for you.

Delta Kunststoffe focuses on the highest quality for all products. We validate all formulations in our own laboratory and technical center before delivery. We test all batches in accordance with generally applicable standards and individual customer requirements. Delta Kunststoffe''s quality management system is certified in accordance with DIN EN ISO 9001:2015.
