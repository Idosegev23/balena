Founded in 1965, The Cannon Group comprises 30+ companies, with a global presence in 40+ countries. It is recognized as a market leader in technologies and plants for polyurethanes, urethane elastomers, and composite materials processing for different industries. The Group designs and manufactures high- and low-pressure dosing, mixing, and chemical storing equipment for molded polyurethane foams, continuous panels, and slabstock production. 
Dedication to continuous innovation now positions Cannon Group as a driving force offering the design, manufacture, servicing, and maintenance of equipment also in other key technology areas including Thermoforming, Aluminum die-casting and rheocasting, Energy - industrial boilers, comprising electric boilers, steam boilers, hot water heaters, heat recovery boilers, thermal oil heaters, and industrial heat pumps, for standard and special applications, Water & Wastewater Treatment - deaerators, condensate treatment and recovery, effluent treatment plants, sanitary water treatment and sewage treatment, injection water treatment, demineralization, produced water;