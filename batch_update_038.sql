HPL Additives Limited
A leader in Polymer Additives Industry  HPL was founded in 1964. Over the last 5 decades, it has established itself as a leader in the area of the of Chemical Blowing Agents (Exothermic and Endothermic) and has increased the product range to include Antioxidants, Azo-Initiators, Chain Extenders and Cross-Linking Agents for polymers, Water Treatment Chemicals, Specialty Chemicals, Hydrazine hydrate and Hydrazine derivatives.
Our product range includes :
1. Mikrofine® - Endothermic and Exothermic Chemical Blowing Agents for Rubbers and Plastics. 
2. Mikrocell™ - Under Construction. 
3. Kinox®- Antioxidants for Polyolefin. 
4. Polyazo® - Solvent & Water Soluble Azo-Initiators (also used as polymerization catalysts) for Acrylics. 
5. Hichem®- Chain Extenders and Cross-Linking Agents for polymers. 
6. Halocom® - Water Treatment , Chemicals and Biocides, Specialty Chemicals (used in the manufacture of pharma and agro intermediates). 
7. Hiclear® - Clarifying agent for Polypropylene (PP). 
8. Higren™ - Oxo-biodegradable additive for plastics biodegrading relatively quickly in different disposal environment. 

The company has four manufacturing sites : three in Faridabad (Haryana) and one in Derabassi (Punjab). All plants are equipped with state-of-the-art instruments ensuring reliable and safe processes. There is a big focus on ensuring safe operating conditions and environment at all plants. 
All technologies have been developed in-house and there are no technical know-how transfer agreements, joint ventures or collaborations for any of the products manufactured by the company. The technology development starts from the R&D and progresses through the Technology Development group, the Projects and Engineering Services group, the Manufacturing group, the Environment, Health and Safety (EHS) group before it is finalized and blue-printed. Inter-departmental interface and ratification ensures safety, reliability, efficiency of processes, excellent product quality due to stringent design inputs and reduces development and commercialization time. 
Built on Research & Development 
The R&D cell is the fountainhead for all research. It is well equipped with state-of-the-art equipment and instruments. It boasts of highly qualified scientists and chemists from leading academic institutions across the country. The R&D cell at HPL is recognized by the Department of Scientific & Industrial Research, Ministry of Science & Technology, Government of India. There are scale up and Computer Aided Design (CAD) facilities. The validation of the process capabilities is self sufficient from the product design to project commissioning. HPL believes in systems and good manufacturing practices. It is an ISO 9001 certified company since 1997. 
Environmentally Conscious 

The QMS ensures job clarity, documented procedure and process control right from raw materials to finished products. Today, HPL has less than 0.2% in-house and less than 0.1% customer non-conformance. Corrective and preventive action systems are in place to continually improve the product, processes and practices.
Safety - A priority 

HPL believes in sustainable and a socially responsible business and has installed an EMS, certified under ISO : 14001 in 2015 by DNV. HPL owes its success to its people and believes that human resource is an important input to its business process. Constant training, skill up-gradation, motivation, transparent policies, clarity of career path, rewards for excellence are some of the tools HPL uses to get the best out of its people. As a step forward in its commitment to provide a safe and healthy work environment to all, HPL has complied with and obtained OHSAS : 18001 certification in 2006.

Global Reach # Distribution network covering Americas & Europe & Direct supply to Far East Asia and Middle East 
HPL has established its position in both domestic and international markets and has a growth rate of approximately 20% a year. Exports to the developed markets of Europe and USA account for almost 50% of the company’s turnover. Today HPL has a sales and distribution network in 52 countries across the world. In the Indian market, HPL has over 75% market share for all the products it manufactures. It has been able to maintain this share in-spite of reduction in import tariffs and in the face of global competition. This is due to company''s focus on Quality, Delivery and Service.

Values Define who we are 
All of HPLAs interaction whether internally with its employees or externally with its customers, suppliers and other parties are based on its value drivers: Adaptability, Versatility, Teamwork, Caring and Dependability.

