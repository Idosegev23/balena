Siegfried Hofmann GmbH Hofmann - Ihr Impulsgeber
The family-owned company offers its international customers innovative solutions in the areas of injection mold construction, mechanical engineering, contract manufacturing and automation for the plastics processing industry with a focus on the medical and automotive sectors. More than 250 employees develop and produce multi-cavity molds and machine solutions in line with the highest product and service quality standards.

Continuous research work ensures new economical technologies. For example, the in-house product BEAD.MACHINE is also impressive thanks to various patents: The particle foam molding machine shines with exceptional product quality, a high degree of design freedom, sustainable operation with resulting cost savings as well as flexible and fast operational readiness.

Founded in 1958, Siegfried Hofmann GmbH (formerly Werkzeugbau Siegfried Hofmann GmbH) has been operating under the brand "Hofmann - Ihr Impulsgeber" since 2017.

