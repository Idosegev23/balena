About Rajoo
At Rajoo the metamorphosis was quick. It all began in 1986 and the last 38 years have witnessed the transformation of a modest beginning in a relatively unknown town of Manavadar (Junagadh) in Gujarat to an expansive global footprint with offices in India and overseas with partners world-over. Well-known in global circles as a mature and respected organisation with a zeal for quality, price consciousness and latest in extrusion technology, Rajoo comes with the right blend of experience, expertise and excellence.

Excellence in Extrusion is the origin, path and destination, defining solutions offered by the company. Solutions include - the widest range of mono and multilayer blown film extrusion lines (up to Nine layers), an impressive range of sheet extrusion lines (up to Seven layers), water quenched downward extrusion lines (up to Seven layers), lines for PE and PS foamed film and sheets (for various standard and special applications) as well as end-to-end thermoforming solutions. Lines for Cross Lamination Films, Extrusion Coating & Lamination and EVA Sheet Extrusion line for solar encapsulation are the recent additions to the product portfolio.    

Skilled and experienced manpower ensure quality in product and after-sales-service.

Infrastructure

Rajoo’s state-of-the-art design and manufacturing facilities in sprawling green acres and built-up area of 40,000 sq. mts. are located on the outskirts of Rajkot, Gujarat, one of the most industrious and vibrant states of India, famous for its engineering skills. World-class integrated facilities comprise R&D, design office, tool-room, die shop, metal treatment shop, fabrication shop, paint shop, assembly shop and testing shop.

Technologies / products categories available include:

·         Mono & Multilayer Blown Film Extrusion Lines  up to 9 layers)
·         Mono & multilayer sheet Extrusion Lines (up to 5 layers)
·         Single & Multi-Station Thermoforming Machine( up to 4 stations)
·         Cross Lamination Film Line for Tarpaulin
·         Extrusion Coating & Lamination Line

The in-house modern paint shop facility is the first of its kind in the Asian industry. An amply dimensioned shot blasting room and baking oven ensures long life of paint and improves machine aesthetics.

The huge and well equipped 3 different assembly shops are partitioned to accommodate machines of different heights and lengths.

To the Rajoo Business Approach, only thing constant being change, the company has constantly looked for avenues to provide the right combination of a diversified product mix so as to create the right solution to suit local needs. The spirit of innovation and adaptation continues to drive the Rajoo growth story.

All this ensuing, strong growth – far exceeding the market growth;

