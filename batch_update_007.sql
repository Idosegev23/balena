Elastever develops thermoplastic elastomer (TPE) formulations from recycled raw materials, in particular recycled thermoset elastomer powders. These recycled raw materials come in particular from the end-of-life tyre and shoe industries.  
We produce these materials in the form of granules, suitable for use in injection, extrusion or moulding.  

The products offered are characterised by :  
very high recycled content (>70%)  
100% recyclability  
A low price, in line with the cheapest elastomers  

The materials are manufactured in our own plant in France.  
The hardness of the materials ranges from 50 ShA to 90 ShA.  
Plastics manufacturers can use their existing machines to process our materials.  

In addition to the standard range, Elastever can develop custom formulations to your requirements. 
