Evonik Industries AG
Evonik is a superforce for global industry. Every day, we go beyond the boundaries of chemistry with our combination of innovative strength and leading technological expertise.

Customer-tailored solutions include high-performance polymers such as PA12, PA 612, PA610, PA1010, and PEBA under the brand VESTAMID®, chemically resistant transparent polyamide TROGAMID®, and VESTAKEEP® PEEK for high temperatures and demanding applications. Along with our pioneering development and best-in-class production of INFINAM® polymer powders for 3D printing, we have a strong focus on enhancing the sustainability of our products, including waste reduction and plastics circularity initiatives.

The specialty additives for masterbatches, compounders and converters include malodor absorbers, crosslinkers, and  dispersing agents for pigments and fillers, as well as processing and performance additives. These modify surface characteristics, improve mechanical properties like scratch resistance, anti-blocking, free flow and anticaking behaviors, and optimize flame retardancy.

Smart Effects in Plastics: Innovative silica and silane technologies are transforming the plastics industry by enhancing performance, processing, and sustainability. SPHERILEX® 30 AB and 60 AB offer anti-blocking effects for high-clarity films, ensuring transparency and easy handling. SIPERNAT® D 17 improves flow and prevents caking in powder systems, while SIPERNAT® 380 supports dispersion and consistency in formulations. For silane crosslinking, Dynasylan® SILFIN 301 and ready-to-use Dynasylan® SILFIN mixtures provide reliable solutions for HFFR compounds and cable production. Dynasylan® HYDROSIL offers reactive, water-based silane hydrolysates, and Dynasylan® Oligomers outperform standard monomers in adhesion and durability.
