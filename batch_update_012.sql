With over 70 years of experience, ZERMA is one of the leading manufacturers of high-quality size reduction equipment. The extensive range of machines covers the entire spectrum of size reduction technology. Starting with beside the press granulators to soundproof granulators, high performance granulators to heavy duty shredders. The product range is rounded off by special machines, pulverizers, suitable accessories and spare parts.

As a result, ZERMA is able to offer solutions for the recycling of most recyclable materials.
ZERMA = shredding and processing of all kinds of plastic waste!

