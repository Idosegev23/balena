SIKOPLAST Recycling Technology GmbH
SIKOPLAST Recycling Technology GmbH is a leading medium-sized engineering company based in Troisdorf, near Cologne. Since 1986, we have been dedicated to developing and manufacturing customized plastic recycling systems – combining decades of expertise with cutting-edge technology. Our origins go back to the early 1970s, driven by a clear mission: to recycle plastic waste sustainably, efficiently, and with maximum material protection.
As a full-service partner, we provide tailor-made solutions for both production scrap and post-consumer plastics. Our product range includes state-of-the-art recycling lines with our conical single-screw extruder, washing plants for heavily contaminated materials, shredders, pelletizing systems, silos, conveyors, and screen changers. One of our specialties is in-line recycling for films and nonwovens – where production waste is fed directly back into the line, eliminating intermediate steps like pelletizing.
What makes our technology stand out is the robust, user-friendly design of our conical screw extruders, ensuring highly reliable operation and gentle material processing. Every plant is engineered to the highest German and European quality standards and is individually tailored to the specific application – whether films, fibers, PET flakes, hollow bodies, HDPE containers, or regrind.
With our in-house pilot plant, customers can test materials under real conditions and jointly develop the most efficient recycling process. From the first project idea through design, manufacturing, installation, and commissioning to training and worldwide after-sales service – we are your partner every step of the way.

Our co-exhibitor at the booth:
Andreas Trouvain – TROUVAIN PARTS-SERVICE & CONSULTING
With over 25 years of industry expertise, Andreas Trouvain offers a trusted source for pelletizer die plates, knives, knife holders, water chambers, and cutting rotors – suitable for all major pelletizer brands. His company is known for top-class quality, competitive pricing, and a deep understanding of customers’ day-to-day challenges, delivering practical and reliable solutions.
Come and visit us at the K Trade Fair in Düsseldorf and discover how SIKOPLAST, together with TROUVAIN PARTS-SERVICE & CONSULTING, can take your recycling processes to the next level – with powerful technology, decades of experience, and an uncompromising focus on quality.
