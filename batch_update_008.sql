·        2-3-4 roll calenders and roller-head solutions
·        Complete lines for fabric frictioning, textile/steel cord coating, and rubber sheeting
·        Automatic wind-up systems, vulcanizing rotary machines, hydraulic presses
·        Full turnkey production systems for tires and conveyor belts
·        Recovery systems for rubber scrap from end-of-life tires
Plastic Division
·        Horizontal, vertical, F and L-type calenders with 2 to 5 rolls
·        Suitable for PVC, TPO, PP film and sheets
·        Full lines for dosing, mixing, and calendering soft, semi-rigid, and rigid plastics
·        Modernization and revamping services for existing calendering plants
 R&D and Innovation
