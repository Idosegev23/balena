ROLF SCHLICHT GmbH
ROLF SCHLICHT GmbH  – K Duesseldorf - 19 – 26 October 2022 – hall 3A35

Quality and competence since 1959

ROLF SCHLICHT GmbH is manufacturer of the RS POWDERTECH powder coating machines and RS MULTICUT cutting systems. Our machines have been successfully in use worldwide for more than 60 years.

The RS POWDERTECH electrostatic and pneumatic powder coating systems stand for absolutely dust-free and finely dosed powdering of cables, wires, hoses, profiles and tapes with powdering agents such as talc, graphite, stearates, swelling powders, organic powders, colour powders etc. Their use results in a clean environment that protects workers as well as machines and aggregates.

Depending on the product specification, either the RSC NG, successor to the RSC / PBA powdering machine that has been used successfully for many years, or the new development from the TALKATOR NG series are used.

With the RS MULTICUT series, ROLF SCHLICHT GmbH offers a wide range of system solutions for versatile cutting tasks in the extrusion sequence. Where cutting is not required, a comprehensive range of semi- or fully automatic winders with strapping, tying, shrinking as well as ejecting or depositing systems on pallets or conveyor belts is available.

The field of application for our systems is the production sector of plastic and rubber extrusion for the manufacturing of hoses, tubes, profiles, strips from the smallest diameter range of less than 1 mm in medical technology up to tubes of approx. 100 mm diameter.

We also supply our feeding and cutting systems in GMP design for the pharmaceutical industry and medical technology for the clean room. The systems work in the extrusion line or also outside the line from the coil, the winder or the pallet.

The systems can be adapted to the customer''s wishes and needs in order to achieve the best possible effectiveness.

Our service guarantees the fast delivery of machines and spare parts as well as technical assistance, also on site.

A well-organized sales network in more than 60 countries guarantees a smooth procurement process thanks to the know-how of the local market.

We further completed our product portfolio with ARISTO Cutting Solutions GmbH. The company has been part of our group of companies since 2021 and offers cutting solutions for cutting in production. The flatbed cutters have been used successfully for decades for textile materials in the transportation, automotive and aviation sectors. Both in individual and in series production. The high quality of the solutions enables reliable and highly precise processing of composites such as carbon, prepreg, fiberglass and other fiber composites.

ARISTOMAT cutters are increasingly used in research, production and development because of their suitability for cutting composite semi-finished products in a wide variety of designs and fabric structures.

SCHLICHT DELIVERY PROGRAM OVERVIEW:

RS POWDERTECH: RSC NG modular system for electrostatic powder coating machines for cables, hoses, profiles and bands.
Talkator NG SPC for a dust-free powdering of cables, hoses, wires and profiles in very slow running processes.
Talkator NG DR for powdering rubber molded parts or granules.

RS MULTICUT: Cutting systems, caterpillar haul-offs, extrusion downstream equipment.  Cutting machines, type MC-NG, orbital cutter OC-NG for pipes, scissor cutting system.
NEW: Modular cutting system MCS - modular construction, for different belt widths and contact lengths.

ARISTO Cutting Solutions GmbH: Flatbed cutters for textile materials in the transportation, automotive and aviation sectors.

Sagitta S.p.A., Italy: Band granulators, transport and depowdering systems. Rubber and plastic slitting machine.

HUESTIS INDUSTRIAL, USA: Air wipes for blowing and drying, cable strippers with feeding and cutting systems.

Friul Filiere S.p.A, Italy: Extruders and extrusion plans for plastic profiles, sheets, bands and pipes.

Battaggion S.p.A, Italy: Sigma and Z-kneaders, granulating systems, turbomixers, high speed mixers, vacuum dryers, planet mixers as well as double screw extruders, mixing mills, extruders for silicone processing.

Molteni S.R.L., Italy: Mixing technology for the chemical industry, food, pharma industry, cosmetics, rubber and plastics. A subsidiary of the Battaggion group.

Valtorta S.R.L., Italy: Turbo mixer, high speed mixer, vacuum dryer, mixing lines, kneader and planetary mixer. A subsidiary of Battaggion group.

NorMec, Norway: Joining and splicing systems for profiles, rings etc. Vulcanizing materials, moulds, handling systems.

F.lli MARIS S.p.A., Italy: Co-rotating twin-screw extruders for plastic and rubber, used for high-quality compounding of plastics and rubber and for recycling or upcycling processes.

Fontijne Pressen, The Netherlands: Hydraulic laboratory presses for quality control in research and development, factories, laboratories and universities worldwide.

Valmet – Lektar Oy, Finnland: Cable pulling belts for caterpillars and pullers to manufacture electric cables.

Wallace Instruments Co. Ltd., Great Britain: Instruments and test equipment for rubber to measure physical properties such as hardness, plasticity, viscosity, curing, compression, etc.

PLASTIBLOW S.R.L., Italy: Electric blow molding machines for food packaging, cosmetics, pharmaceuticals, industrial chemicals or detergents.

PUMATECH S.R.L., Italy: Rubber-bale cutters, strip cutters, cryogenic deflashing systems.

