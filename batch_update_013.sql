Bekum Maschinenfabriken GmbH
WELCOME TO BENCHMARK FOR BLOW MOLDING MACHINES

Pioneer and market leader in extrusion blow molding technology
Since its foundation in Berlin in 1959, the Bekum Group has been a global pioneer in extrusion blow molding technology. The name "Bekum" was coined by company founder Gottfried Mehnert (†2022) as an acronym for Berliner Kunststoff Maschinen. In its founding year, the company developed the world''s first ring surface calibration - the beginning of numerous technical innovations and patents that are now industry standards.
Machine solutions for individual plastic packaging
Bekum develops and produces modular all-electric, hybrid and hydraulic extrusion blow molding machines in a modern and multi-award-winning industrial design for the production of hollow packaging from 5 ml to 3,000 liters. Applications range from PET bottles, pharma & medical, household & personal care, dairy & food, canisters, drums and large volume containers, lubricants & chemicals, automotive & mobility as well as technical parts and special solutions with multiple layers of material.
With the latest 8 series and the intelligent BC 8.0 machine control system, Bekum is setting standards in automation, process reliability and energy efficiency
Philosophy and values
As a second-generation family business, managing partner Michael Mehnert pursues an independent, future-oriented and reliable corporate policy based on long-term partnerships with customers, employees and suppliers. The combination of tradition, technical expertise and innovative strength enables continuous growth and quality that far exceeds the standard.
Technology & Innovation
Bekum machines are characterized by process reliability, high availability, energy efficiency and durable design. These standards include numerous technologies developed in-house and key components manufactured in-house in extrusion, mechanical engineering and automation - combined with high repeatability and ROI-oriented system design.
Locations & employees
Bekum employs around 300 people at four locations:
- Berlin, Germany (Holding, Development, Sales & Service)
- Traismauer, Austria (development, large-scale and packaging machine production, service)
- Williamston, MI USA (production, sales & service for North America)
There is also a sales and service office in Siegburg near Bonn to provide customers with flexible regional support.

Markets & references
With over 18,000 installed systems (about half of which are still in active use) in over 100 countries, Bekum is one of the industry leaders.


