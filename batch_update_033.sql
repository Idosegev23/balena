Thermoware Particle Foam Machinery is the worldwide market leader of complete production lines for thin walled particle foam products. With our technology and service we serve a wide range of industries. Our machinery is used to produce packaging applications for food, agriculture and medical & pharma, but also for technical parts in automotive, lost foam and  sports & leisure.

