BIOTEC Biologische Naturverpackungen GmbH & Co. KG
BIOTEC is a leading company that develops and manufactures sustainable bioplastics from renewable plant-based resources. The application range of films as well as molded parts extends from garbage bags or shopping bags to capsules for the pharmaceutical industry, to display packaging for the food industry, cosmetic packaging and numerous other customer-specific requirements. Under the brand name BIOPLAST, BIOTEC produces and sells a new generation of customized thermoplastic materials with a wide range of functional properties. All products made from BIOPLAST are 100% biodegradable.

The company''s main focus is to develop materials with the highest possible proportion of bio-based raw materials. The biobased content of our product range starts at 23% (BIOPLAST GF 106/02), increases to over 50% (BIOPLAST 500) and finally reaches 69% (BIOPLAST GS 2189). However, our flagship product is BIOPLAST TPS® with the highest possible biobased content of 100%.

With its dedicated research team, BIOTEC is constantly working to improve the product properties (mechanics, barrier, etc.) of BIOPLAST to meet the needs of industries such as cosmetics, food, agriculture, pharmaceuticals and others.

BIOTEC is one of the pioneers in the rapidly growing bioplastics market, offering credible and sustainable alternatives to conventional plastics on an industrial scale. Furthermore, our company is committed to significantly reducing the use of fossil resources. Through an extensive distribution network, BIOTEC is represented in most European countries and offers customer service as well as technical support at the highest level.
