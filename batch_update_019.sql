ROWA Lack GmbH
Proven brand products and individual brands: basic components for a successful result.

ROWA GmbH was founded in 1958 in Seevetal near Hamburg as a rubber processing company. From 1969, the company also produced high-quality coating systems, matting agent concentrates and additive systems for the coating and plastics industries.

Since 2010, as an independent company within the ROWA GROUP, ROWA Lack convinces with experience from more than half a century. Thanks to technical competence, state-of-the-art product design and customer-oriented service, ROWA Lack is currently one of the market leaders in the field of surface finishing of coated textiles.

With a branch in China - Ningbo ROWA Coatings Technology in Ningbo City - the company has been consolidating its service quality there since 2011 after many years of market presence.

Together with its customers, ROWA Lack develops customized solutions - because customer satisfaction comes first. Whether it is the development of new, innovative coating systems or the optimization of coating processes, ROWA Lack always offers flexibility and personal support. In the case of new developments, the focus is particularly on the search for environmentally friendly raw materials in combination with state-of-the-art technology.
