KEBO AG Injection Mould Technology
KEBO stands for highest quality and lowest part costs

We specialize in injection moulding tools for demanding customer requirements. We realize the shortest cycle times even with high cavities.
Our development department combines 40 years of experience with the latest technologies. Through intensive contact with our international OEM customers, we are constantly adapting our technology to meet the high demands of our customers in terms of quality and productivity. KEBO has its own hot runner technology, which is used and adapted to specific applications.
Our engineers start with the customer''s idea and develop it into a highly productive tool. In doing so, they draw on the latest simulation tools and a wealth of experience. KEBO has its own pilot molds for all injection situations, which are made available to our customers for faster and more cost-effective development of sample parts.
In Neuhausen, Switzerland, we produce highly productive tools that are in demand around the globe. The high vertical range of manufacture in our own plant, a modern production infrastructure and flat structures guarantee the customer a reliable and long-term investment.
Finally, comprehensive tool tests are carried out in our in-house technical center, which we document accordingly. Of course, we also support our customers with the validation processes.
The durability of our tools and our in-house maintenance service are highly valued by our customers. KEBO - we take care.
