Erema Engineering Recycling Maschinen und Anlagen GmbH
PREMIUM
Another life for plastic. Because we care.
EREMA Engineering Recycling Maschinen und Anlagen Ges.m.b.H. has its headquarters in Ansfelden near Linz in Austria and specialises in the development and manufacturing of plastics recycling systems and components. We are manufacturin innovative recycling technologies for the processing of plastics in the form of inhouse waste, bottle-to-bottle, post consumer and fibres to produce recycled pellets that meet the highest quality specifications. Also in our portfolio: recycling systems for processing input steams mechanically for optimal chemical recycling.
Some 7,000 EREMA recycling systems are now in use worldwide. In total thei produce more than 18.5 million tonnes of plastics pellets every year.

The product range in detail:
Plastics recycling systems for standard applications and production waste
Plastics recycling systems for heavily printed/contaminated post-consumer waste (FDA-approved for HDPE recycling)
Plastics recyling system CHEMAREMA® for processing input steams mechanically for optimal chemical recycling
PET recycling systems VACUREMA® and VACUNITE® (FDA, efsa and brand owner approved, e.g. for bottle to bottle recycling)
Inline PET applications (e.g. for fibre, film, strapping and preforms)
COREMA® plastics recycling system for the production of highly filled and customised compounds
Fully automatic, self-cleaning melt filters
Pelletising systems
