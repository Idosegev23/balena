Promeco S.p.A.
Founded in 1996, Promeco SpA is an Italian Company based in Como that manufactures, on proprietary technology, plants and machinery for waste recycling and recovery with energy production, material recovery or production of finished goods.

Innovation, R&D, passionate and skilled team along with the customer satisfaction have always been the success of Promeco and they still are the peculiarities of the Company.

Besides the historical Promeco-patented equipment, like single and double shafts shredders, extruders, densifiers, briquetting machines, separators, squeezers and centrifuges, the Company offers a wide range of turn-key plants and customized solutions , among which:

plants for chemical recycling of mixed plastic waste
plant for pre-treatmet of feedstock for chemical recycling plants
plants and machinery for mechanical recycling of plastics
briquetting and pelletizing plants
shredding plants
plants for RDF production
plants for the production of second raw material and finished products
plants for recycling of waste pulper
plants for wood fiber production from green waste and biomass
plants for the pretreatment of the organic matter of MSW for anaerobic digestion
Promeco plants are composed for 80% by machinery at 100% designed, constructed and assembled by the same Promeco, which is still the customer after-sale referent thanks to its well­ stocked storage.

The constant R&D allows the development of new processes in response to specific requirements of the waste problem, which requires to anticipate future lifestyle and environmental laws which are constantly changing.

The Company policy focuses on product quality: robustness and functionality of the machines that maintain their value over time.

The ability to challenge ideas and concepts and to interact with partners worldwide has allowed and still allows Promeco to stay highly competitive and to be an ideal partner for the world market.
