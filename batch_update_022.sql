ITENE Packaging, Transport & Logistics Research Center
ITENE is a leading research and technology center specialized in packaging, transport, and logistics. For over 30 years, we have supported companies worldwide through R&D projects, testing services, technical assistance, and training, providing technological solutions that drive innovation and sustainability across the entire packaging value chain.

We help companies incorporate circularity into their packaging strategies, working from material development to end-of-life. Our team designs and formulates new packaging materials with specific goals such as improving technical performance, integrating recycled content, reducing material use, and enhancing the functionality of biopolymers. These developments always consider industrial scalability and economic viability.

We also assess both new and existing packaging solutions from a variety of perspectives, including food safety, reusability, recyclability, compostability, and distribution efficiency. Based on these evaluations, we adapt and optimize packaging systems to meet functional and regulatory requirements while improving their environmental profile.

In terms of end-of-life, we develop and improve a wide range of technologies including mechanical, chemical, and enzymatic recycling, as well as systems for composting and reuse. Our research extends to innovative approaches that enable the recovery of complex packaging materials and the transformation of waste into valuable raw materials with high-quality properties.

Headquartered in Valencia (Spain), ITENE operates in over 12,000 square meters of its own facilities, including 5,000 square meters of laboratories and pilot plants equipped with advanced technology. These infrastructures allow us to validate the performance and functionality of packaging and materials under real and simulated conditions.

Between 2018 and 2024, more than 2,000 companies relied on our team of over 200 highly qualified professionals. We work with clients and partners internationally, serving a wide range of industries such as food and beverage, cosmetics, chemicals, pharmaceuticals, logistics, distribution, recycling, and raw materials.

At ITENE, we innovate, create, and grow—empowering the packaging ecosystem to meet the challenges of today and lead the transformation of tomorrow.
