Exolon Group: Passionate about plastic sheets

Exolon Group is a leading supplier of solid and multiwall sheets for various innovative applications. The company develops, produces and distributes polycarbonate sheets, polyester sheets and opaque sheet material made of high-quality plastic. 

Our performance promise:
Going beyond!
is our daily business, whether it is about the development of our products or the personal service we provide to our customers, to realize their needs. We are an inspiring partner with a human touch, that is putting our clients in the lead by listening, understanding, providing solutions and better experiences. We show what is and what is possible. And we make it happen.

As an innovation-driven company, we are constantly looking for advanced solutions to realise your ideas and wishes in a wide range of applications - and to open up new application possibilities for you. Our solid and multiwall sheets of the Exolon®, AkyVer ® , Axpet®, Vivak®, Inspria® and Curvalite® brands are convincing in the areas of building & construction, industry, visual communication, LED lighting and medical technology.

New name with 40 years of experience
Even though the name is still young, Exolon Group has over 40 years of expertise in the extrusion of thermoplastic sheets. Exolon Group combines proven tradition with forward-looking innovations. Since 2020, the company has been part of the Serafin Group based in Munich.

Strong, clear, flexible.
In order to maintain its leading role, Exolon Group is therefore constantly driving new developments and conquering new business areas – close to the market and close to the customer. We are strong and confident about our skills, expertise and experience. Clear and straightforward in the way we behave and speak and flexible in our mindset. Including the selection and compilation of suitable raw materials, the ongoing optimization of production processes, the continuous training of employees and the promotion of innovative technologies.
