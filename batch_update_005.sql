BYK-Chemie GmbH
BYK is a leading global supplier of specialty chemicals. The company’s innovative additives and differentiated solutions optimize product and material properties as well as production and application processes. Among other things, BYK additives improve the scratch resistance and gloss of surfaces, the mechanical strength or the flow behavior of materials, and properties such as light resistance and flame retardancy.
BYK has an extensive product portfolio with which it supports customers in the plastics industry. Plastics processors, com-pounders, formulators, and OEMs make use of the vast selection of high-performance additives for processing advanced com-posites, PVC plastisols, and a wide range of thermoplastic materials. Amongst others, these include lightweight materials for the automotive industry, reinforced plastics for wind turbines, and weather-resistant plastics for outdoor applications such as fa-cades, stadium seating, outdoor furniture, and greenhouses.

Besides this, BYK additives also promote greater comfort when, for example, they optimize PVC flooring or ensure flame retardancy, scratch resistance, and odor reduction. Together, the world’s largest network of application laboratories, its technical service, and its customer focus form the basis for BYK’s profound in-sights into market demands and trends.

BYK has a global network of branches and operates production sites in Germany (Wesel, Kempen, Schkopau, Moosburg, Ger-etsried), the Netherlands (Deventer, Denekamp, Nijverdal), and the UK (Widnes), in the USA (Wallingford, Chester, Columbia, Earth City, Gonzales, Louisville, Pittsford) and in China (Shanghai, Tongling). The company is part of the ALTANA Group and employs more than 2,600 people worldwide.
