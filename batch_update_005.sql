Addiplast Group S.A.S.
Addiplast Group is a family-owned French company and a European leader in the design and production of high-performance thermoplastic compounds and color & fonctional masterbatches. Since 1986, we have combined cutting-edge R&D capabilities with flexible manufacturing technologies to deliver innovative, sustainable solutions for industries such as automotive, mobility, electronics, healthcare, beauty, packaging, and consumer goods.

At the heart of our innovation strategy lies Addiscience®, our dedicated R&D center, bringing together engineers, chemists, and color specialists. With over 400 new formulations developed annually, we create tailor-made materials with enhanced mechanical, thermal, and aesthetic properties. Our expertise in polymer science, flame retardancy, coloration, and surface effects enables us to deliver advanced compounds that meet the most demanding industrial and regulatory requirements.

Sustainability is embedded in our DNA. Through our ADDIBIO Renew® range, we offer bio-based, recycled, and biodegradable materials to support the transition to a circular economy. From replacing glass fibers with natural fibers such as Miscanthus to developing halogen-free flame-retardant compounds, we are actively reducing environmental impact and supporting green chemistry initiatives.

With three production sites and two R&D centers in France, a total capacity of 50,000 tons/year, and certifications including ISO 14001, ISO 9001, and IATF 16949, Addiplast Group is committed to quality, innovation, and eco-responsibility. We invest 5% of our revenue in R&D to anticipate market needs and develop sustainable materials that lower carbon footprint while delivering uncompromising performance and design flexibility.

Our product portfolio includes Addiflam® flame-retardant compounds, Addibio Renew® for eco-friendly solutions, and color & functional masterbatches, offering our global clients the perfect balance between technical excellence and environmental responsibility.
